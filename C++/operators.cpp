#include<iostream>
using namespace std;

int main(){
    int a,b;
    cout<<"Enter a :";
    cin>>a;
    cout<<"Enter b :";
    cin>>b;
    //Arithmetic Operators
    cout<<"The value of a + b is : "<<a+b<<endl;
    cout<<"The value of a - b is : "<<a-b<<endl;
    cout<<"The value of a * b is : "<<a*b<<endl;
    cout<<"The value of a / b is : "<<a/b<<endl;
    cout<<"The value of a % b is : "<<a%b<<endl;
    cout<<"The value of a ++ is : "<<a++<<endl;
    cout<<"The value of a -- is : "<<a--<<endl;
    cout<<"The value of ++ a is : "<<++a<<endl;
    cout<<"The value of -- a is : "<<--a<<endl;
    cout<<endl;

    //Comparison operators
    cout<<"The value of a == b is : "<<(a==b)<<endl;
    cout<<"The value of a != b is : "<<(a!=b)<<endl;
    cout<<"The value of a >= b is : "<<(a>=b)<<endl;
    cout<<"The value of a <= b is : "<<(a<=b)<<endl;
    cout<<"The value of a > b is : "<<(a>b)<<endl;
    cout<<"The value of a < b is : "<<(a<b)<<endl;
    

}